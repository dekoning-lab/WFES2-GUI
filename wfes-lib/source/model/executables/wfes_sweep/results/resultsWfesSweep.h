#ifndef RESULTSWFESSWEEP_H
#define RESULTSWFESSWEEP_H

#include <QStandardPaths>
#include <QTextStream>
#include <QDir>

#include <ctime>

#include <boost/math/special_functions/fpclassify.hpp>

#include "utils/types.h"

#include <model/executables/wfes_sweep/config/configWfesSweep.h>

class ResultsWfesSweep {
    public:
        /**
         * @brief Expected number of generations between two fixation events (Fixation mode).
         */
        double tFix;

        /**
         * @brief Rate of substitution.
         */
        double rate;

        /**
         * @brief Execution time.
         */
        double time;

        /**
         * @brief String containing possible errors during execution.
         * This is later shown to the user in a MessageDialog.
         */
        std::string error;

        /**
         * @brief Empty constructor of ResultsWfesSweep.
         */
        ResultsWfesSweep();

        /**
         * @brief Constructor of ResultsWfesSweep with execution time.
         * @param time Execution time.
         */
        ResultsWfesSweep(double time);

        /**
         * @brief Constructor of ResultsWfesSweep when an error happens during execution.
         * @param error Description of the error or message generated by the error.
         */
        ResultsWfesSweep(std::string error);

        /**
         * @brief Constructor of wfes single with fixation mode.
         * @param t_fix Expected number of generations between two fixation events.
         * @param rate Rate of substitution.
         * @param time Execution time.
         */
        ResultsWfesSweep(double t_fix, double rate, double time);

        /**
         * @brief Write results in a CSV file.
         * @param results results to be printed in a CSV file.
         * @param name Name of the file.
         */
        void writeResultsToFile(ResultsWfesSweep *results, std::string name);

};

#endif // RESULTSWFESSWEEP_H
